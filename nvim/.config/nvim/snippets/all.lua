local ls = require("luasnip")
local s = ls.snippet
local t = ls.text_node
local i = ls.insert_node

return {
  -- Section header
  s("section", {
    t("# ============================================================================="),
    t({ "", "# " }),
    i(1, "SECTION NAME"),
    t({ "", "# =============================================================================" }),
    t({ "", "" }),
    i(0),
  }),

  -- Subsection header

  s("subsection", {
    t("# -----------------------------------------------------------------------------"),
    t({ "", "# " }),
    i(1, "Subsection Name"),

    t({ "", "# -----------------------------------------------------------------------------" }),
    t({ "", "" }),
    i(0),
  }),
}
